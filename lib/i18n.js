// Simple useTranslation hook - returns English text that Google Translate will translate
export const useTranslation = () => {
  const t = (key, params) => {
    // Get the translation or fallback to the key itself
    let text = translations[key] || key.split('.').pop().replace(/_/g, ' ')

    // Handle interpolation if params is an object with key-value pairs
    if (params && typeof params === 'object' && !Array.isArray(params)) {
      Object.entries(params).forEach(([paramKey, value]) => {
        // Replace {{key}} or {key} patterns with the value
        text = text.replace(new RegExp(`\\{\\{?${paramKey}\\}\\}?`, 'g'), String(value))
      })
    }

    return text
  }

  return { t }
}

// Named export for compatibility
export { useTranslation as default }
